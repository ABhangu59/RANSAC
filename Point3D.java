//Start of Point3D Class
public class Point3D {

    //Initializing needed variables
    private double x;
    private double y;
    private double z;

    //Constructor
    public Point3D(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    //Getters for X, Y, Z
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

}
